#include "Gestor.hpp"
#include <cstdlib>
#include <ctime>

std::vector<Pedido> Gestor::generar12PedidosAleatorios() {
    std::vector<Pedido> pedidos;
    for (int i = 0; i < 12; i++) { // Genera 12 pedidos aleatorios 
        Pedido pedido;
        pedidos.push_back(pedido); // Agrega un pedido al final del vector pedidos 
    }
    return pedidos;
}

